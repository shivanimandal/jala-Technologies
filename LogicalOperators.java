//5. Programs on Logical AND,OR operator and Logical NOT

class LogicalOperators
{
	public static void main(String args[]) {
		boolean a = true;
		boolean b = false;
		System.out.println("a && b = "+(a&&b));
		System.out.println("a || b = "+(a||b));
		System.out.println("!(a && b) = "+!(a&&b));
	}
}