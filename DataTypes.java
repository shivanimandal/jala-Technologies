/*4. Define variables for different Data Types int, Boolean, char, float, double and print on the
Console*/

public class DataTypes 
{
	public static void main(String[] args) {
		//Datatypes declaration and assigned the values
		short my_variable = 10;
		byte my_byte = 127;
		int my_int = 40;
		long my_long = 15000000000L;
		float my_float = 4.5f;
		double my_double = 11.56;
		char my_char = 'A';
		boolean is_true = false;
		System.out.println("short = "+my_variable);
		System.out.println("Byte = "+my_byte);
		System.out.println("Int = "+my_int);
		System.out.println("Long = "+my_long);
		System.out.println("Float = "+my_float);
		System.out.println("Double = "+my_double);
		System.out.println("Char = "+my_char);
		System.out.println("Boolean = "+is_true);
	}
}
		
	